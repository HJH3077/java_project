package com.ict08.network;

import java.net.InetAddress;
import java.net.UnknownHostException;

public class Ex01 {
	public static void main(String[] args) {
		// InetAddress : 자바에서 IP주소를 표현하는 클래스
		//				 생성자가 존재하지만 사용하지 않는다.
		//				 6개의 static 메소드를 이용한다.
		//		** Localhost : 현재 내가 사용하고 있는 컴퓨터
		
		// IO이후로는 거의다 try ~ catch ~ 사용함.
		// host = 서비스 주는 사람
		// 도메인은 IP주소를 알기 쉽게 영문으로 표기한 것
		// 그것이 도메인네임시스템 DMS로 이름 체계를 통해 정함
		// .net, .com, .org / www, http 등
		
		try { 
			InetAddress addr = InetAddress.getByName("www.naver.com"); // address이름 변경
			// 이것으로 인해 원래는 223.130.195.95를 입력해야 네이버를 가지만 www.naver.com을 쓸 수 있게됨.
			System.out.println("이름 : " + addr.getHostName());
			System.out.println("주소 : " + addr.getHostAddress()); 
			System.out.println("toString : " + addr.toString());
			System.out.println("===============================================");
			
			
			// 내 컴퓨터에 관한 정보
			addr = InetAddress.getLocalHost();
			System.out.println("이름 : " + addr.getHostName());
			System.out.println("주소 : " + addr.getHostAddress()); 
			System.out.println("toString : " + addr.toString());
			System.out.println("===============================================");
			
			InetAddress[] addrs = InetAddress.getAllByName("m.naver.com");
			for (InetAddress k : addrs) {
				System.out.println("이름 : " + k.getHostName());
				System.out.println("주소 : " + k.getHostAddress()); 
				System.out.println("toString : " + k.toString());
			}
			System.out.println("===============================================");
			
			// 125.209.222.137
			byte[] b = {125, (byte)209, (byte)222, (byte)137};
			addr = InetAddress.getByAddress(b);
			System.out.println("이름 : " + addr.getHostName());
			System.out.println("주소 : " + addr.getHostAddress()); 
			System.out.println("toString : " + addr.toString());
			
			
		} catch (UnknownHostException e) {
			e.printStackTrace();
		} 
		
		
		
		
		
	}
}
